package casting;

public class ImplicitCasting {

	public static void main(String[] args) 
	{
	 /* Converting one type of information into another type is 
	  * called casting.
	  * implicit casting=widening casting
	  * lower-->higher
	  * memory size-->goes on increasing
	  * data loss-->no data loss	
	  */
       
		int a=10; // lower data type information
		System.out.println(a);
		
		double b=a; // higher data type information
		System.out.println(b);
		
	 
	}

}
